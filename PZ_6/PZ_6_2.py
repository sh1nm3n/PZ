'''
Вариант 9.
Дан список размера N. Найти количество участков, на которых его элементы
монотонно убывают.
'''
import random


N = 10
numbers = [random.randint(1, 10) for x in range(N)]
print("Сгенерированный список:", numbers)

#подсчет участков, где элементы монотонно убывают
count = 0

for i in range(1, len(numbers)):
    #проверяем, начинается ли новый участок убывания
    if numbers[i] < numbers[i - 1]:
        #если это первый элемент убывающего участка
        if i == 1 or numbers[i - 1] >= numbers[i - 2]:
            count += 1

print("Количество участков, где элементы монотонно убывают:", count)

